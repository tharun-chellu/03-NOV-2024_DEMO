//switch examples for selection statements

let month = "march";

switch(month) {
    case "january":
        console.log("It is the fist month of year");
        break;
    case "april":
        console.log("it is 4th month of the year");
        break;
    case "may":
        console.log("It is 5th month of the year and it's my birthday");
        break;
    case "november":
    case "december":
        console.log("it is the last months of the year");
    default:
        console.log("It is remaining months of the year");

}









